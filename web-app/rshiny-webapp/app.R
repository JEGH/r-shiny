#________________________________________________________________________________________________________
# Configurations ----
#_________________________________________________________________________________________________________
source('app_config.R', local = TRUE)
#________________________________________________________________________________________________________
# Load Packages ------ 
#_________________________________________________________________________________________________________
source(load_path)
#________________________________________________________________________________________________________
# Utilities     ------ 
#_________________________________________________________________________________________________________
source(utils_path)
#__________________________________________________________________________________________________________________________________
#Dashboard Header ----
#___________________________________________________________________________________________________________________________________
header <- dashboardHeader(uiOutput(headerName))
header$children[[2]]$children <-  uiOutput(headerName_Children)
#__________________________________________________________________________________________________________________________________
#Dashboard Sidebar ----
#___________________________________________________________________________________________________________________________________
sidebar <- dashboardSidebar(uiOutput(sidebarPanelName))
#__________________________________________________________________________________________________________________________________
#Dashboard Body----
#___________________________________________________________________________________________________________________________________
body    <- dashboardBody(uiOutput(bodyName), includeCSS("www/custom-index.css"))
                                                 
#__________________________________________________________________________________________________________________________________
#User Interface Mount  ----
#___________________________________________________________________________________________________________________________________
ui <- dashboardPage(skin = colorSkin , header, sidebar, body,useShinyjs())
#__________________________________________________________________________________________________________________________________
#Server Mount ----
#___________________________________________________________________________________________________________________________________
server <- function(input, output, session) {
  source(paste0(controller_path ,controller_name),          local = TRUE)
  source(paste0(ui_path         ,ui_name        ),          local = TRUE)
  options(options_server)
}
#__________________________________________________________________________________________________________________________________
# Run the application
shinyApp(ui = ui, server = server,options = options_shiny )
